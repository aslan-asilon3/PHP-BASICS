function eucDistance(array $a, array $b) {
    return
    array_sum(
        array_map(
            function($x, $y) {
                return abs($x - $y) ** 2;
            }, $a, $b
        )
    ) ** (1/2);
}
// now use it with any array
// make sure both arrays have the same number of elements
echo round(eucDistance([7,6], [3,5]),2);


